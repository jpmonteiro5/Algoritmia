public class Calculadora {
    //metodos de calculo
    public double adicao(double a, double b){
        return a+b;
    }
    public double subtracao(double a, double b){
        return a-b;
    }
    public double multiplicacao(double a, double b){
        return a*b;
    }
    public double divisao(double a, double b){
        return a/b;
    }

}
